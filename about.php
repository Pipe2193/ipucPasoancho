                            <p style="text-align: center;">
                                <span style="font-size: 14pt;"><strong><span style="color: #008080;">(Romanos 5:12,19).</span></strong></span></p>

                            <h4 class="page-header"  style="color: #ffcd22">LA GRACIA DE DIOS Y LA SALVACIÓN DEL HOMBRE</h4>
                            <p>La gracia es el favor inmerecido por el cual Dios rescata al hombre habilitándole para una vida nueva, pasando por alto en su paciencia los pecados pasados. "Siendo justificados gratuitamente por su gracia, mediante la redención que es en Cristo Jesús, a quién Dios puso como propiciación por medio de la fe en su sangre, para manifestar su justicia, a causa de haber pasado por alto, en su paciencia, los pecados pasados. (Romanos 3:24-25). "Porque la gracia de Dios se ha manifestado para salvación a todos los hombres, enseñándonos que, renunciando a la impiedad y a los deseos mundanos, vivamos en este siglo sobria, justa y piadosamente" (Tito 2:11-12). "Pues la ley por medio de Moisés fue dada, pero la gracia y la verdad vinieron por medio de Jesucristo" (Juan 1:17). El cristiano debe guardarse en la gracia y en el amor de Dios para conservar su salvación. Cuando una persona comete trasgresión y peca contra Dios, pierde su favor. Y si continúa cometiendo pecado y no se arrepiente, final mente se perderá y será lanzado al lago de fuego. (Léase Juan 152:2; 2 Pedro 2:20-21). Judas habla de los que tornaron atrás y de su recompensa (Léase Hebreos 6:4-6) "Porque por gracia sois salvos por medio de la fe; y esto no de vosotros, pues es donde Dios"(Efesios2:8).</p>
                        </div>
                        <div class="col-sm-6 text-justify" data-animate="fadeInLeft" data-delay="300">
                            <h4 class="page-header"  style="color: #ffcd22">LA FE</h4>
                            <p>Es, pues la fe, la certeza de lo que se espera, la convicción de lo que no se ve (Hebreos 11:1).  La fe es el resultado de oír o recibir el evangelio de la gracia de Dios. "Por quien también tenemos entrada por la fe a esta gracia en la cual estamos firmes, y nos gloriamos en la esperanza de la gloria de Dios. Porque si siendo  enemigos, fuimos reconciliados con Dios por medio de la muerte de su Hijo, mucho más, estando reconciliados, seremos salvos por su vida. Pues si por la trasgresión de uno soto reinó la muerte, mucho más reinarán en vida por uno solo, Jesucristo, los que reciben la abundancia de la gracia y del don de la justicia. (Romanos 5:2, 10,17). </p>

                            <p style="text-align: center;">
                                <span style="font-size: 14pt;"><span style="color: #077aaf;">"Y por quien recibimos la gracia y el apostolado, para la obediencia a la fe en todas las naciones por amor de su nombre" </span></span></p>
                            <p style="text-align: center;">
                                <span style="font-size: 14pt;"><strong><span style="color: #008080;">(Romanos 1:5)</span></strong></span></p>

                            <h4 class="page-header"  style="color: #ffcd22">ARREPENTIMIENTO Y CONVERSIÓN</h4>
                            <p>La palabra "arrepentimiento" viene de varias palabras griegas que significan cambio de miras y propósitos,  cambio de corazón, cambio de mente, cambio de vida, transformación, etc. "Deje el impío su camino, y el hombre inicuo sus pensamientos, y vuélvase a Jehová, el cual tendrá de él misericordia, y al Dios nuestro, el cual será amplio en perdonar". (Isaías 55:7; Lucas 13:1-5). El arrepentimiento genuino es la confesión y abandono de los pecados. Juan el bautista predicó el arrepentimiento. Jesús lo proclamó, y los apóstoles lo enfatizaron tanto a judíos como a gentiles. </p>
                            <p style="text-align: center;">
                                <span style="font-size: 14pt;"><span style="color: #077aaf;">"Pedro les dijo: Arrepentíos, y bautícese cada uno de vosotros en el nombre de  Jesucristo para perdón de tos pecados; y recibiréis el don del Espíritu Santo". </span></span></p>
